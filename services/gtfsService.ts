// Helper to parse CSV data
function parseCsv<T>(csvText: string): T[] {
    const lines = csvText.trim().split('\n');
    const header = lines[0].split(',').map(h => h.trim());
    return lines.slice(1).map(line => {
        const values = line.split(',');
        return header.reduce((obj, key, index) => {
            (obj as any)[key] = values[index]?.trim() || '';
            return obj;
        }, {} as T);
    });
}

// Type definitions for raw GTFS parsing
interface RawStop {
    stop_id: string;
    stop_name: string;
    stop_lat: string;
    stop_lon: string;
}

interface RawShapePoint {
    shape_id: string;
    shape_pt_lat: string;
    shape_pt_lon: string;
    shape_pt_sequence: string;
}

// Cache the processed data to avoid re-computation
let cachedData: { busStops: BusStop[]; routePath: Point[] } | null = null;

export function getFukutsuGtfsData(): { busStops: BusStop[]; routePath: Point[] } {
    if (cachedData) {
        return cachedData;
    }

    const rawStops = parseCsv<RawStop>(stopsData);
    const rawShapes = parseCsv<RawShapePoint>(shapesData)
      .sort((a, b) => parseInt(a.shape_pt_sequence) - parseInt(b.shape_pt_sequence));

    const allPoints = [
        ...rawStops.map(s => ({ lat: parseFloat(s.stop_lat), lon: parseFloat(s.stop_lon) })),
        ...rawShapes.map(s => ({ lat: parseFloat(s.shape_pt_lat), lon: parseFloat(s.shape_pt_lon) }))
    ].filter(p => !isNaN(p.lat) && !isNaN(p.lon));

    const minLat = Math.min(...allPoints.map(p => p.lat));
    const maxLat = Math.max(...allPoints.map(p => p.lat));
    const minLon = Math.min(...allPoints.map(p => p.lon));
    const maxLon = Math.max(...allPoints.map(p => p.lon));

    const latRange = maxLat - minLat;
    const lonRange = maxLon - minLon;
    
    // Use a small margin to prevent points from being on the very edge
    const margin = 5; 
    const mapWidth = 100 - 2 * margin;
    const mapHeight = 100 - 2 * margin;

    const normalize = (lat: number, lon: number): Point => {
        // Adjust for aspect ratio to avoid distortion
        const aspectRatio = lonRange / latRange;
        let scaleX = mapWidth;
        let scaleY = mapHeight;
        let offsetX = margin;
        let offsetY = margin;

        if (aspectRatio > 1) { // Wider than tall
            scaleY = mapWidth / aspectRatio;
            offsetY = margin + (mapHeight - scaleY) / 2;
        } else { // Taller than wide
            scaleX = mapHeight * aspectRatio;
            offsetX = margin + (mapWidth - scaleX) / 2;
        }

        const x = lonRange === 0 ? offsetX + scaleX / 2 : offsetX + ((lon - minLon) / lonRange) * scaleX;
        // Invert Y-axis because screen coordinates start from top-left
        const y = latRange === 0 ? offsetY + scaleY / 2 : offsetY + (1 - (lat - minLat) / latRange) * scaleY;
        return { x, y };
    };
    
    const busStops: BusStop[] = rawStops.map(s => {
        const lat = parseFloat(s.stop_lat);
        const lon = parseFloat(s.stop_lon);
        return {
            id: s.stop_id,
            name: s.stop_name,
            lat,
            lon,
            position: normalize(lat, lon)
        };
    });
    
    const routePath: Point[] = rawShapes.map(s => {
        const lat = parseFloat(s.shape_pt_lat);
        const lon = parseFloat(s.shape_pt_lon);
        return normalize(lat, lon);
    });

    cachedData = { busStops, routePath };
    return cachedData;
}